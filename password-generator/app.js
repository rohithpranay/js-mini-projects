let passGenBtn = document.querySelector("#pass-gen");
let passDispBtn1 = document.querySelector(".pass-1");
let passDispBtn2 = document.querySelector(".pass-2");

function randomPasswordGen() {
  let password = "";
  for (let i = 0; i < 16; i++) {
    let indx = Math.floor(Math.random() * characters.length);
    password += characters[indx];
  }
  return password;
}

passGenBtn.addEventListener("click", () => {
  let pass1 = randomPasswordGen();
  let pass2 = randomPasswordGen();
  passDispBtn1.textContent = pass1;
  passDispBtn2.textContent = pass2;
});
